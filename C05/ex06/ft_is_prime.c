int	ft_is_prime(int nb)
{
	int	m;

	m = 2;
	if (nb == 1 || nb == 0 || nb < 0)
		return (0);
	while (m < (nb / 2))
	{
		if ((nb % m == 0) && nb != m)
			return (0);
		else
			m++;
	}
	return (1);
}
